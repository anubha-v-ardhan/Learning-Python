# String Concatenation => Adding Strings Together

print("20 Days are " + str(50) + " minutes")  # In python, we need to change no. to String ourselves like this

print(f"20 Days are {50} minutes") # Better syntax (new addition in python3)

print(f"20 days are {20 * 24 * 60} minutes")
print(f"20 days are {20 * 24 * 60 * 60} seconds")

print(f"30 days are {30 * 24 * 60} minutes")